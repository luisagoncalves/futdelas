package org.ifb.tcc.futdelas.entities;

public class Rodada {
    private String nome;
    private String slug;
    private int rodada;
    private String status;

    public String getNome() {
        return nome;
    }
    public String getSlug() {
        return slug;
    }
    public int getRodada() {
        return rodada;
    }
    public String getStatus() {
        return status;
    }
}